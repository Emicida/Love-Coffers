        <p> O grão de café cru não tem um gosto agradável. Por isso ele precisa passar por mudanças físico-químicas que desenvolvem seus sabores e aromas característicos. Ou seja, ele precisa ser torrado para tornar-se palatável. Para controlar o ponto de torra, o profissional acompanha visualmente a evolução da cor dos grãos por um coletor de amostras, ou pelo controle por perfil de torra com o auxílio da tecnologia. Os atributos sensoriais da bebida estão intimamente relacionados com o grau de torra do café.< Logo abaixo estão os três principais tipos de torra:</p>
        <OL>
          <LI TYPE=I><b>Torra clara: </b> Acentua acidez e aroma com suavidade do sabor, ameniza o amargor e o corpo. Preserva mais os óleos aromático, mas acentua a acidez da bebida ao mesmo tempo que produz um café gourmet menos encorpado.<br>
          <LI TYPE=I><b>Torra média: </b>Ponto de equilíbrio entre várias características como acidez, aroma e amargor.  Normalmente acentua o corpo.  Porém, o café gourmet começa a ficar menos encorpado com torras mais escuras.<br>
          <LI TYPE=I><b>Torra escura </b>Café menos ácido, mais amargo e menos encorpado.  Torra mais escura deixa o café gourmet mais amargo o que pode ser confundido com um café “mais forte”. Pode “queimar” o café, acentuando muito o amargor e praticamente eliminando a acidez e o corpo.<br>
        </OL>

    <h1>Das arábias para o mundo</h1>
    <p>A história começa pela criação do nome, que tem origem árabe. A planta era conhecida como Kaweh e a bebida foi denominada como Kahwah ou Cahue, que significa Força. Originária do continente africano, das regiões altas da Etiópia (Cafa e Enária), onde ocorre espontaneamente como planta de sub-bosque. Segundo uma das "lendas" da descoberta do cafeeiro, um pastor etíope foi quem percebeu que algumas de suas cabras mudaram seu comportamento após fazer uso de folhas da planta de café em sua alimentação, influenciando no comportamento de monges que o observaram.</p>
    <p>Da Etiópia foi levado para a Arábia. Os árabes tentaram manter o privilégio, pois foram os primeiros a cultivar essa planta "milagrosa" que assumia grande importância social devido ao seu uso na medicina da época para a cura de diversos males. Da Arábia o café foi levado primeiramente para o Egito no século XVI e logo depois para Turquia. Na Europa, no século XVII, foi introduzido na Itália e na Inglaterra. Logo depois passou a ser consumido em vários outros países europeus, chegando à França, Alemanha, Suíça, Dinamarca e Holanda.</p>
    <p>Seguindo sua marcha de expansão pelo mundo, o café chegou nas Américas e nos Estados Unidos, atualmente o maior consumidor e importador mundial de café. Foram os holandeses que disseminaram o café pelo mundo. Inicialmente transformaram suas colônias nas Índias Orientais em grandes plantações de café e junto com franceses e portugueses transportaram o café para a América.</p>
    <p>Desde a chegada ao Brasil até a consolidação como modelo econômico, passaram-se 100 anos. As produções no país começaram em modestas lavouras, mas com o passar do tempo, o chamado ouro negro foi responsável pelo alvorecer da esperança de um recomeço para Portugal. Foi somente no séc XIX, na região do Vale do Rio Paraíba, que as plantações de café no Brasil ganharam maior representatividade. Devido a escassez do ouro e a alta concorrência do açúcar, pois era necessário encontrar alternativas que  superassem os problemas econômicos e contribuíssem para a manutenção da aristocracia. Assim, a expansão do café no Brasil surgiu como uma oportunidade para a continuidade do Império e do Primeiro Reinado.</p> 
    <p> A vinda da corte foi decisiva,inclusive, para o desenvolvimento da economia cafeeira e a industrialização no país, que registrou  o início da  abertura dos portos para o comércio internacional e estimulou as atividades industriais que favoreceram, sobretudo, a urbanização. </p>

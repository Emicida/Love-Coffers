      <DT><b>Café em grão </b></DT><br>
      <DD>No café em grão, o único processo a que os grãos são submetidos é a torrefação. Para o preparo em casa é necessária uma máquina de café automática com moinho integrado, ou um moinho independente para fazer a moagem do café antes de este ser colocado numa máquina de café espresso ou de filtro. A principal vantagem é o fato de ser a única forma de manter todas as propriedades do café sem qualquer alteração no seu aroma, sabor e teor de cafeína. Se o café for moído e não for consumido na hora, é preciso guarda num recipiente hermético para conserva.</DD><br>
      <DT><b>Café em Pó</b></DT><br>
      <DD>Este é o tipo de café mais comumente utilizado para consumo doméstico, e pode ser utilizado em qualquer tipo de máquina de café, com exceção de máquinas de cápsula. Uma vez que é previamente moído, perde algumas das suas propriedades antioxidantes, assim como o seu aroma e algum do seu sabor. Por outro lado, permite a extração da cafeína, o que possibilita a criação da versão “descafeinado”. O seu preço é mais acessível que o café em grão e é de fácil acesso. </DD><br>
      <DT><b>Café em cápsulas</b></DT><br>
      <DD>Considerada a forma mais cómoda e rápida de preparo, seu principal benefício é a simplicidade e a limpeza de todo o processo, além da variedade de opções, com diferentes níveis de intensidade, sabor e cafeína. A atualmente, a oferta de cápsulas de café é bem variada, contando ainda com outro tipo de opções, como bebidas com leite, chás ou outro tipo de combinações.</DD><br>
      <DT><b> Café moído em pastilhas</b></DT><br>
      <DD>Com uma forma rápida, cómoda e limpa de preparo, não mais do que café moído em saquetas, semelhantes às utilizadas para chás e infusões, com o formato do manípulo da própria máquina para facilitar a utilização. Uma das desvantagens é o preço – a dose individual tem um valor maior, comparando ao café em pó ou em cápsulas, assim como, disponibilidade e uma menor variedade de marcas e sabores.</DD> 
